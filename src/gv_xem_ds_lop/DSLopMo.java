    private void btnNhanLopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNhanLopActionPerformed
        // TODO add your handling code here:
        String maLop = ds.get(idx).getMa_lop();
        Lop lop = dbLop.getLop(maLop);
        if(lop.getMa_gv() != 0) {
            JOptionPane.showMessageDialog(null, "Lớp này đã có giảng viên dạy", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return ;
        }
        if(!giangVienLopDB.checkLichDay(maLop, maGv)) {
            JOptionPane.showMessageDialog(null, "Lớp này trùng lịch với một lớp đã đăng ký dạy", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return ;
        }
        dbLop.gvNhanLop(maLop, maGv);
        HienThi();
    }//GEN-LAST:event_btnNhanLopActionPerformed

