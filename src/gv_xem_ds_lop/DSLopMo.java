    private void btnHuyNhanLopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHuyNhanLopActionPerformed
        // TODO add your handling code here:
        String maLop = ds.get(idx).getMa_lop();
        Lop lop = dbLop.getLop(maLop);
        if(lop.getMa_gv() != maGv) {
            JOptionPane.showMessageDialog(null, "Bạn không thể hủy lớp này", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return ;
        }
        dbLop.gvHuyLop(maLop);
        HienThi();
    }//GEN-LAST:event_btnHuyNhanLopActionPerformed

